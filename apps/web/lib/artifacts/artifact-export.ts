/**
 * Generate a professional Word (.docx) document from artifact data.
 * Returns a Uint8Array (the .docx binary).
 */
export async function generateArtifactDOCX(input: ArtifactExportInput): Promise<Uint8Array> {
  const { artifact, contract, tenantName } = input;
  const children: Paragraph[] = [];

  // Title
  children.push(
    new Paragraph({
      children: [
        new TextRun({
          text: formatArtifactTitle(artifact.type),
          bold: true,
          size: 36,
          color: '2563EB',
          font: 'Calibri',
        }),
      ],
      heading: HeadingLevel.TITLE,
      spacing: { after: 200 },
    }),
  );

  // Subtitle
  const subtitleParts = [
    contract?.fileName,
    contract?.contractType ? `Type: ${contract.contractType}` : null,
    new Date().toLocaleDateString('en-US', { year: 'numeric', month: 'long', day: 'numeric' }),
  ].filter(Boolean);
  
  children.push(
    new Paragraph({
      children: [
        new TextRun({
          text: subtitleParts.join('  •  '),
          size: 20,
          color: '64748B',
          font: 'Calibri',
        }),
      ],
      spacing: { after: 300 },
    }),
  );

  // Metadata
  const metaParts: string[] = [];
  if (artifact.confidence != null) metaParts.push(`Confidence: ${Math.round(Number(artifact.confidence) * 100)}%`);
  if (artifact.qualityScore != null) metaParts.push(`Quality: ${artifact.qualityScore}/100`);
  if (artifact.modelUsed) metaParts.push(`Model: ${artifact.modelUsed}`);
  
  if (metaParts.length > 0) {
    children.push(
      new Paragraph({
        children: [
          new TextRun({ text: metaParts.join('  •  '), size: 18, color: '94A3B8', italics: true }),
        ],
        spacing: { after: 400 },
      }),
    );
  }

  // Horizontal rule
  children.push(new Paragraph({
    border: { bottom: { color: 'E2E8F0', style: BorderStyle.SINGLE, size: 1, space: 1 } },
    spacing: { after: 300 },
  }));

  // Content
  renderDocxContent(children, artifact.type, artifact.data);

  const doc = new Document({
    creator: tenantName || 'ConTigo AI',
    title: formatArtifactTitle(artifact.type),
    description: `AI-generated ${artifact.type.toLowerCase()} analysis`,
    sections: [
      {
        properties: {
          page: {
            margin: { top: 1440, right: 1440, bottom: 1440, left: 1440 },
          },
        },
        children,
        footers: {
          default: new Footer({
            children: [
              new Paragraph({
                alignment: AlignmentType.CENTER,
                children: [
                  new TextRun({ text: 'Generated by ConTigo AI  •  Page ', size: 16, color: '94A3B8' }),
                  new TextRun({ children: [PageNumber.CURRENT], size: 16, color: '94A3B8' }),
                  new TextRun({ text: ' of ', size: 16, color: '94A3B8' }),
                  new TextRun({ children: [PageNumber.TOTAL_PAGES], size: 16, color: '94A3B8' }),
                ],
              }),
            ],
          }),
        },
      },
    ],
  });

  const buffer = await Packer.toBuffer(doc);
  return new Uint8Array(buffer);
}

function renderDocxContent(children: Paragraph[], type: string, data: Record<string, any>) {
  function heading(text: string, level: 1 | 2 = 1) {
    children.push(
      new Paragraph({
        children: [
          new TextRun({
            text,
            bold: true,
            size: level === 1 ? 28 : 24,
            color: level === 1 ? '1E293B' : '334155',
            font: 'Calibri',
          }),
        ],
        heading: level === 1 ? HeadingLevel.HEADING_1 : HeadingLevel.HEADING_2,
        spacing: { before: level === 1 ? 400 : 200, after: 100 },
      }),
    );
  }

  function body(text: string) {
    children.push(
      new Paragraph({
        children: [new TextRun({ text: String(text || ''), size: 22, color: '334155', font: 'Calibri' })],
        spacing: { after: 100 },
      }),
    );
  }

  function keyVal(key: string, value: any) {
    if (value == null || value === '') return;
    const valStr = typeof value === 'object' ? JSON.stringify(value) : String(value);
    children.push(
      new Paragraph({
        children: [
          new TextRun({ text: `${key}: `, bold: true, size: 22, color: '64748B', font: 'Calibri' }),
          new TextRun({ text: valStr, size: 22, color: '334155', font: 'Calibri' }),
        ],
        spacing: { after: 60 },
      }),
    );
  }

  function bullet(text: string) {
    children.push(
      new Paragraph({
        children: [new TextRun({ text, size: 22, color: '334155', font: 'Calibri' })],
        bullet: { level: 0 },
        spacing: { after: 40 },
      }),
    );
  }

  // Type-specific rendering
  switch (type) {
    case 'OVERVIEW': {
      const val = (k: string) => data[k]?.value ?? data[k];
      heading('Contract Overview');
      keyVal('Contract Name', val('contractName') || val('title'));
      keyVal('Type', val('contractType'));
      keyVal('Status', data.status);
      keyVal('Effective Date', val('effectiveDate'));
      keyVal('Expiration Date', val('expirationDate'));
      keyVal('Total Value', formatMoney(data.totalValue));
      keyVal('Governing Law', val('governingLaw'));

      if (data.parties?.length) {
        heading('Parties', 2);
        for (const p of data.parties) keyVal(p.role || 'Party', p.name);
      }
      if (val('executiveSummary')) {
        heading('Executive Summary', 2);
        body(val('executiveSummary'));
      }
      break;
    }

    case 'CLAUSES': {
      heading('Key Clauses');
      for (const clause of data.clauses || data.items || []) {
        heading(clause.title || clause.name || 'Clause', 2);
        if (clause.category) keyVal('Category', clause.category);
        if (clause.riskLevel) keyVal('Risk Level', clause.riskLevel);
        body(clause.fullText || clause.text || clause.summary || '');
      }
      break;
    }

    case 'FINANCIAL': {
      heading('Financial Summary');
      keyVal('Total Value', formatMoney(data.totalValue));
      if (data.paymentTerms) {
        heading('Payment Terms', 2);
        if (typeof data.paymentTerms === 'string') {
          body(data.paymentTerms);
        } else {
          keyVal('Net Days', data.paymentTerms.netDays);
          keyVal('Method', data.paymentTerms.paymentMethod);
        }
      }
      if (data.fees?.length) {
        heading('Fees', 2);
        for (const fee of data.fees) {
          bullet(`${fee.name}: ${formatMoney(fee)} (${fee.frequency || 'one-time'})`);
        }
      }
      break;
    }

    case 'RISK': {
      heading('Risk Assessment');
      if (data.overallRiskScore) {
        keyVal('Overall Score', `${data.overallRiskScore.value}/10 (${data.overallRiskScore.category})`);
      }
      for (const risk of data.risks || data.items || []) {
        heading(risk.title || risk.name || 'Risk', 2);
        keyVal('Severity', risk.severity || risk.level);
        keyVal('Category', risk.category);
        body(risk.description || '');
        if (risk.mitigation || risk.recommendation) {
          keyVal('Mitigation', risk.mitigation || risk.recommendation);
        }
      }
      break;
    }

    default: {
      heading(formatArtifactTitle(type));
      for (const [key, value] of Object.entries(data)) {
        if (key === 'extractionMetadata') continue;
        if (Array.isArray(value)) {
          heading(key.replace(/([A-Z])/g, ' $1').trim(), 2);
          for (const item of value.slice(0, 30)) {
            const display = typeof item === 'string'
              ? item
              : item.title || item.name || item.description || JSON.stringify(item);
            bullet(display);
          }
        } else if (typeof value === 'object' && value !== null) {
          keyVal(key, value.value ?? JSON.stringify(value));
        } else {
          keyVal(key, value);
        }
      }
    }
  }
}
