/**
 * Artifact Document Export Service
 *
 * Generates professional PDF and DOCX documents from artifact data.
 * Uses jspdf (PDF) and docx (DOCX) libraries already in dependencies.
 *
 * Supported formats: PDF, DOCX, JSON, CSV (existing)
 *
 * @version 1.0.0
 */

import { jsPDF } from 'jspdf';
import {
  Document,
  Packer,
  Paragraph,
  TextRun,
  HeadingLevel,
  Table,
  TableRow,
  TableCell,
  WidthType,
  AlignmentType,
  BorderStyle,
  ShadingType,
  Footer,
  PageNumber,
  NumberFormat,
} from 'docx';

// =============================================================================
// TYPES
// =============================================================================

export interface ArtifactExportInput {
  artifact: {
    id: string;
    type: string;
    data: Record<string, any>;
    confidence?: number | null;
    qualityScore?: number | null;
    completenessScore?: number | null;
    createdAt?: string | Date;
    updatedAt?: string | Date;
    modelUsed?: string | null;
  };
  contract?: {
    fileName?: string | null;
    contractType?: string | null;
    parties?: string[];
  };
  tenantName?: string;
}

